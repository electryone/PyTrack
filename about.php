							<p style="padding-left: 50px;">
							How flying animals use visual information to navigate complex environments is an open question in biology. We are investigating this problem by using a flight simulator
							that allows a Manduca sexta moth to navigate a virtual forest environment. I developed a computer vision software tool, PyTrack, which is capable of automatically
							tracking flight behaviors related to motions of the legs and abdomen. PyTrack can also be used for simple position tracking tasks, and is simpler to use than other
							more complex and/or expensive solutions (OpenCV, MATLAB, etc.).</p>
							
							After the completion of the project, the code was rewritten for more general computer vision tracking tasks. PyTrack is currently being continued as a research project 
							at Morehouse, to develop PyTrack as a more general tool for academic biological research. This project will explore finding, implementing, and optimizing computer vision
							algorithms to fit this purpose. This research will also explore generalized and specific behavior tracking and identifying for various animals. 
